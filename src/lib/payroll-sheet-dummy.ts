export function getPayrollSheetDummy(): PayrollSheetRow[] {
  return rows.map((r, i) => ({ ...r, id: `ps-${i + 1}` }));
}

export function getPayrollSheetKpi(rows: PayrollSheetRow[]) {
  return {
    employees: rows.length,
    gross: rows.reduce((a, r) => a + r.gross, 0),
    deductions: rows.reduce((a, r) => a + r.deductions, 0),
    netPay: rows.reduce((a, r) => a + r.netPay, 0),
  };
}

export const formatPayrollInr = (n: number) =>
  `₹${(n || 0).toLocaleString("en-IN", { maximumFractionDigits: 0 })}`;
